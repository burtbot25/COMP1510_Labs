package ca.bcit.comp1510.lab1;

/**
 * This program counts from one to five in English, French, and Spanish
 * 
 * @author Wilburt Herrera
 * @version 2017
 */
public class Count {

    /**
     * Prints a message in 3 separate languages
     * 
     * @param args
     *            command line arguments (unused)
     */
    public static void main(String[] args) {
        
        // // prints one to five in English
        System.out.println("one two three four five");
        
        // prints one to five in French
        System.out.println("un deux trois quatre cinq");
        
        // prints one to five in Spanish
        System.out.println("uno dos tres cuatro cinco");
    }

}
